  /// Get all unique categories
  static List<String> get categories {
    return allExercises
        .map((e) => e.category)
        .whereType<String>()
        .toSet()
        .toList()
      ..sort();
  }

  /// Get all unique movements
  static List<String> get movements {
    return allExercises
        .map((e) => e.movement)
        .whereType<String>()
        .toSet()
        .toList()
      ..sort();
  }
