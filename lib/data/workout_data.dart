/// Helper to create workout exercises by ID
WorkoutExercise _we(String id, int sets, int reps, int order) {
  return WorkoutExercise(
    exercise: ExerciseLibrary.getById(id),
    sets: sets,
    reps: reps,
    order: order,
  );
}

